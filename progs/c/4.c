#include	<stdio.h>


int main ()
{
	int a = 5;
	int b = 2;
	
	printf("La suma es %d", a+b);
	printf("\n");
	printf("La resta es %d", a-b);
	printf("\n");
	printf("La multiplicación es %d", a*b);
	printf("\n");
	printf("La divisón es %d", a/b);
	printf("\n");

	return 0;
}				/* ----------  end of function main  ---------- */
